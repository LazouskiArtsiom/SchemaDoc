    private static void AppendScript(StringBuilder sb)
    {
        sb.AppendLine("<script>");
        sb.AppendLine(@"
function filterTables(q) {
  q = q.toLowerCase();
  document.querySelectorAll('.table-section').forEach(s => {
    s.style.display = s.dataset.name.includes(q) ? '' : 'none';
  });
  document.querySelectorAll('.toc-item').forEach(li => {
    li.style.display = li.dataset.name.includes(q) ? '' : 'none';
  });
}
");
        sb.AppendLine("</script>");
    }
